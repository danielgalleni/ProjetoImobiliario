    public void preencherTable(List<Cliente> lista){
                
        DefaultTableModel modelo = (DefaultTableModel)jTableAgendamento.getModel();
        modelo.setNumRows(0);
        
        for(Cliente c: lista){
            modelo.addRow(new Object[]{c.getCodigo(),c.getNome(),c.getCpf()});
        }
    }
